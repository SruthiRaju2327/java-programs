package Java1stage;

public class Ninstaticvariable {
int i=10;//non static variables are within class out side of method 
          //variables without using static keyword known as non static variables
 static int j=20;//variable using static keyword known as static variables//
 public static void main(String[]args) {
	 int k=30;//within method using variables are called local variables 
	 System.out.println(k);
	 System.out.println(j);
 }
}


